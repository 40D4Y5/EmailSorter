filename = input("Enter name with file extention format: ")

file = open( filename, 'r', encoding='utf8')
email = []
for i in file.readlines():
    email.append(re.findall(r'[\w\.-]+@[\w\.-]+', i))


hf = open('hotmail.txt', 'w')
gf = open('gmail.txt', 'w')
yf = open('yahoo.txt', 'w')
af = open('aol.txt', 'w')
xf = open('yandex.txt', 'w')
pf = open('protonmail.txt', 'w')
mf = open('icouldmail.txt', 'w')
rf = open('domainmail.txt', 'w')
for h in email:
    emailstr  = ''
    emailstr = ''.join(h)
    if '@hotmail.'  in emailstr or "@live." in emailstr or "@outlook." in emailstr:
        hf.write(emailstr+'\n')
    elif '@gmail.' in emailstr:
        gf.write(emailstr+'\n')
        # print ('it is gmail '+ emailstr)
    elif 'yahoo.' in emailstr:
        yf.write(emailstr+'\n')
        # print ('it is yahoo '+ emailstr)
    elif '@aol.' in emailstr or '@love.' in emailstr or '@wow.' in emailstr or '@games.' in emailstr or '@ygm.' in emailstr:
        af.write(emailstr+'\n')
        # print ('it is Aol '+ emailstr)
    elif '@yandex.' in emailstr:
        xf.write(emailstr+'\n')
        # print ('it is Yandex')
    elif '@protonmail.' in emailstr or '@pm.' in emailstr:
        pf.write(emailstr+'\n')
        # print ('it is Proton')
    elif '@icloud.' in emailstr or '@me.' in emailstr or '@mac.' in emailstr:
        mf.write(emailstr+'\n')
        # print ('it is Apple')
    else:
        rf.write(emailstr+'\n')



